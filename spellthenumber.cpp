#include<stdio.h>
int main()
{
    int num;
    printf("Enter a number betn 0-9\n");
    scanf("%d",&num);
    if(num == 0){
        printf("ZERO");
    }
    if(num == 1){
        printf("ONE");
    }
    if(num == 2){
        printf("TWO");
    }
    if(num == 3){
        printf("THREE");
    }
    if(num == 4){
        printf("FOUR");
    }
    if(num == 5){
        printf("FIVE");
    }
    if(num == 6){
        printf("SIX");
    }
    if(num == 7){
        printf("SEVEN");
    }
    if(num == 8){
        printf("EIGHT");
    }
    if(num == 9){
        printf("NINE");
    }
    return 0;
}